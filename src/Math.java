public class Math {
    boolean isEven(int a) {
        boolean result = (a % 2) == 0;
        return result;
    }

    boolean isOdd(int a) {
        boolean result = (a % 2) == 1;
        return result;
    }

    double circleField(double a) {
        double result = a * 3.14;
        return result;
    }

    int power(int a) {
        int result = a * a;
        return result;
    }

}
